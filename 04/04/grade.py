chemi= 91                                  # 定义变量，存储化学成绩的分数
physi = 75                                 # 定义变量，存储物理成绩的分数
biolog = 84                                # 定义变量，存储生物成绩的分数
math = 84                                 # 定义变量，存储数学成绩的分数
print("化学：" + str(chemi) + " 物理:" +str(physi) + " 数学:" +str(math) + " 生物:" +str(biolog)+"\n")
print("物理 > 化学：" + str(chemi > physi))                    # 大于操作
print("数学 < 化学：" + str(physi < biolog))                    # 小于操作
print("数学 == 生物的结果：" + str(math == biolog))         # 等于操作
print("物理 != 生物的结果：" + str(physi != biolog))      # 不等于操作
print("数学 <= 化学的结果：" + str(math<= chemi))       # 小于等于操作
print("生物 >= 物理的结果：" + str(biolog >= physi))        # 大于等于操作
